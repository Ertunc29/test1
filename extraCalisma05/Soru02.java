package extraCalisma05;

public class Soru02 {

	public static void main(String[] args) {
		
		// 0 dan 20 ye kadar olan sayilari for loop ile yazalim
		
	  
		for (int i=0; i<=20;i++) {
			System.out.print(i+" ");
		}
		
		System.out.println();
		
		for (int i=20;i>=0;i--) {
			System.out.print(i+" ");
		}

	}

}
